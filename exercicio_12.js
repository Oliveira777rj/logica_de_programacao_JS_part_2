// armazene um valor de velocidade em uma variavel e verifique a velocidade permitida atraves do if/else

let velocidade = 120;

if(velocidade <= 80){
    console.log(`Sua velocidade é de ${velocidade}, está dentro do limite permitido, boa viagem!`)
}else{
    console.log(`Sua velocidade é de ${velocidade}, está fora do limite permitido!`)
}